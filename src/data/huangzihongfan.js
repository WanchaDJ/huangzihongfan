export function normalizeEvent(raw) {
  return {
    ...raw,
    coverImage: raw.coverImage || concertPhoto,
    status: normalizeStatus(raw.status),
    priceTiers: normalizePriceTiers(raw.priceTiers)
  }
}

export function normalizeStatus(status) {
  if (!status) return 'UPCOMING'
  const value = String(status).toLowerCase()
  if (['onsale', 'on_sale', 'on-sale'].includes(value)) return 'ON_SALE'
  if (['soldout', 'sold_out', 'sold-out'].includes(value)) return 'SOLD_OUT'
  if (['ended', 'finish', 'finished'].includes(value)) return 'ENDED'
  if (['cancelled', 'canceled'].includes(value)) return 'CANCELLED'
  if (['presale', 'pre_sale'].includes(value)) return 'PRESALE'
  return String(status).toUpperCase()
}

export function normalizePriceTiers(priceTiers) {
  if (Array.isArray(priceTiers)) return priceTiers
  if (!priceTiers) return []
  try {
    const parsed = JSON.parse(priceTiers)
    if (Array.isArray(parsed)) {
      return parsed.map((item) => {
        if (typeof item === "number") return { price: item, description: '票档' }
        return item
      })
    }
  } catch {
    return []
  }
  return []
}

export function formatDateTime(dateValue) {
  if (!dateValue) return '时间待公开'
  const date = new Date(dateValue)
  if (Number.isNaN(date.getTime())) return String(dateValue)
  const y = date.getFullYear()
  const m = String(date.getMonth() + 1).padStart(2, '0')
  const d = String(date.getDate()).padStart(2, '0')
  const hh = String(date.getHours()).padStart(2, '0')
  const mm = String(date.getMinutes()).padStart(2, '0')
  return `${y}-${m}-${d} ${hh}:${mm}`
}

export function openExternal(url) {
  if (!url) return
  window.open(url, '_blank', 'noopener,noreferrer')
}
